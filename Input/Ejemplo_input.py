#Pedir dos números y convertirlos en decimal con la variable float
var1=float(input("Introduce el primer número: "))
var2=float(input("Introduce el segundo número: "))

#Sumar las dos variables y asignarlas a un total
total=var1+var2

#Mostrar en pantalla los dos valores escogidos y las suma de los dos.
print("El valor introducido por teclado es:",var1)
print("El segundo valor introducido es:",var2)
print("La suma de los dos números es:",total)
